/*
No JS é muito comum usarmos de operações aritméticas para a criação de algoritmos. Portanto, nesta aula, veremos alguns deles.
*/


/*
    1º. Adição
    
Como o próprio nome já sugere, é literalmente uma adição como usamos normalmente em nosso dia-a-dia.
*/

var ex1 = 2
var ex2 = 3
var res1 = ex1 + ex2

console.log(res1)


/*
    2º. Subtração
    
É a diminuíção de um valor, também é muito comum e usado no dia-a-dia.
*/

var ex3 = 10
var ex4 = 9
var res2 = ex3 - ex4

console.log(res2)

/*
    3º. Multiplicação
    
Multiplicação é um número vezes outro número, bem comum também.
*/

var ex5 = 2
var ex6 = 5
var res3 = ex5 * ex6

console.log(res3)

/*
    4º. Divisão
    
Divisão é um número dividido por outro número.
*/

var ex7 = 10
var ex8 = 2
var res4 = ex7 / ex8

console.log(res4)


/*
    5º. Módulo
    
Módulo é bem parecido com a divisão, mas nele usaremos o sinal '%' e o que ele retornará para nós, será sempre o resto da divisão, quando ele existir.
*/

var ex9 = 11
var ex10 = 10
var res5 = ex9 % ex10

console.log(res5)


/*
    6º. Incremento
    
O incremento é básicamente uma adição escrita de uma forma mais prática, normalmente é escrito o nome da variável seguido de ++. Os sinais de ++ podem ser escritos antes da variável, depende bastante da ocasião que você está utilizando.
*/

var exemplo1 = 9
exemplo1++

console.log(exemplo1)

/*
    7º. Decremento
    
Decremento seria o incremento de uma forma negativa, ou seja, retirando valores. A mesma lógica é utilizada aqui.
*/

var exemplo2 = 10
exemplo2--

console.log(exemplo2)
